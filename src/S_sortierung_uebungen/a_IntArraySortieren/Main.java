package a_IntArraySortieren;

import java.util.Arrays;

public class Main {
    public static void main(String[] args) {
        System.out.println("Int Array sortieren");

        //Zahlen sortieren (in Array)
        int[] zahlen = {5, 2, 8, 1, 9, 4};
        Arrays.sort(zahlen);
        System.out.println(Arrays.toString(zahlen));

        //Zeichen sortieren (In Array) ----
        char[] zeichen = {'h', 'H', 'l', 'l', '5', 'L'};
        Arrays.sort(zeichen);
        System.out.println(Arrays.toString(zeichen));
//
        //       Dog[] hunde = {
        //               new Dog(7, 15),
        //               new Dog(3, 20)
    }

    ;

}